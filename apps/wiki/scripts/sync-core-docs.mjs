// Syncs core settings documentation from apps/riven/docs/settings.md
// into the wiki configuration page, grouped by wiki.section metadata
// from the Zod schema.
//
// Run: node scripts/sync-core-docs.mjs
// Requires: pnpm turbo codegen:config-docs to have run first (generates settings.md)
import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, unlinkSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";

const ROOT = resolve(import.meta.dirname, "../../..");
const RIVEN_APP = join(ROOT, "apps/riven");
const SETTINGS_MD = join(RIVEN_APP, "docs/settings.md");
const OUTPUT_FILE = join(
  import.meta.dirname,
  "../content/docs/configuration/index.mdx",
);

// Section metadata: slug → display config
// Order here determines section order in the output.
const SECTIONS = [
  { slug: "database", title: "Database" },
  { slug: "logging", title: "Logging" },
  { slug: "graphql", title: "GraphQL Server" },
  { slug: "vfs", title: "VFS" },
  { slug: "scraping", title: "Scraping & Downloads" },
  { slug: "ranking", title: "Ranking" },
  { slug: "scheduling", title: "Scheduling" },
  { slug: "database-ssl", title: "Database SSL" },
  { slug: "plugins", title: "Plugins" },
  { slug: "danger-zone", title: "Danger Zone" },
];

// --- Step 1: Import the Zod schema to get field → section mapping ---

function getFieldSections() {
  // Use Node's --experimental-strip-types to import the TS schema from apps/riven.
  // Write a temp script file to avoid shell escaping issues with -e.
  const tmpScript = join(RIVEN_APP, "_sync-core-docs-tmp.mjs");
  const script = [
    `import { RivenSettings } from './lib/riven-settings.schema.ts';`,
    `const result = {};`,
    `for (const [key, field] of Object.entries(RivenSettings.shape)) {`,
    `  const meta = field.meta?.() || {};`,
    `  result[key] = meta['wiki.section'] || 'uncategorized';`,
    `}`,
    `process.stdout.write(JSON.stringify(result));`,
  ].join("\n");

  writeFileSync(tmpScript, script);
  try {
    const output = execFileSync(
      "node",
      ["--experimental-strip-types", tmpScript],
      {
        cwd: RIVEN_APP,
        encoding: "utf-8",
      },
    );
    return JSON.parse(output);
  } finally {
    try {
      unlinkSync(tmpScript);
    } catch {}
  }
}

// --- Step 2: Parse the generated settings.md table ---

function parseSettingsTable(mdContent) {
  const lines = mdContent.split("\n");

  // Find the table: starts with | Property |
  const tableStart = lines.findIndex((l) => l.startsWith("| Property"));
  if (tableStart === -1) throw new Error("Could not find settings table");

  const headerRow = lines[tableStart];
  const separatorRow = lines[tableStart + 1];

  // Parse data rows
  const rows = [];
  for (let i = tableStart + 2; i < lines.length; i++) {
    const line = lines[i];
    if (!line.startsWith("|")) break;

    // Extract the property name from the first cell
    // Format: `propertyName` or **`propertyName`** (*)
    const cells = line
      .split("|")
      .filter((_, idx, arr) => idx > 0 && idx < arr.length - 1);
    const propCell = cells[0]?.trim() || "";
    const match = propCell.match(/`(\w+)`/);

    if (match) {
      rows.push({ key: match[1], line });
    }
  }

  return { headerRow, separatorRow, rows };
}

// --- Step 3: Group rows by section and generate MDX ---

function generateMdx(fieldSections, table) {
  const { headerRow, separatorRow, rows } = table;

  // Group rows by section
  const grouped = new Map();
  for (const section of SECTIONS) {
    grouped.set(section.slug, []);
  }

  for (const row of rows) {
    const section = fieldSections[row.key] || "uncategorized";
    if (!grouped.has(section)) {
      grouped.set(section, []);
    }
    grouped.get(section).push(row.line);
  }

  // Build MDX content
  const parts = [
    `---
title: Configuration Overview
description: All Riven TS settings and environment variables.
---

import { Callout } from "fumadocs-ui/components/callout";

{/* This file is auto-generated by scripts/sync-core-docs.mjs — do not edit manually. */}

Riven TS is configured entirely through environment variables. All core settings use the prefix \`RIVEN_SETTING__\` and plugin settings use \`RIVEN_PLUGIN_SETTING__REPO_PLUGIN_<NAME>__\`.

## Core Settings`,
  ];

  for (const section of SECTIONS) {
    const sectionRows = grouped.get(section.slug);
    if (!sectionRows || sectionRows.length === 0) continue;

    const headingTitle = section.link
      ? `[${section.title}](${section.link})`
      : section.title;
    parts.push(`\n### ${headingTitle}`);

    if (section.slug === "danger-zone") {
      parts.push(`
<Callout type="error">
  These settings will destroy data. Only use them if you know what you're doing.
</Callout>`);
    }

    parts.push("");
    parts.push(headerRow);
    parts.push(separatorRow);
    parts.push(...sectionRows);
  }

  // Add the plugin settings reference section
  parts.push(`
## Plugin Settings

Plugin settings follow the pattern:

\`\`\`
RIVEN_PLUGIN_SETTING__REPO_PLUGIN_<NAME>__<settingName>="value"
\`\`\`

For example:

\`\`\`bash
RIVEN_PLUGIN_SETTING__REPO_PLUGIN_TMDB__apiKey="your-key"
RIVEN_PLUGIN_SETTING__REPO_PLUGIN_MDBLIST__lists=["user/list"]
\`\`\`

<Callout>
  Array values are passed as JSON strings: \`["value1", "value2"]\`
</Callout>

See the [Plugins](/docs/plugins) section for each plugin's available settings.
`);

  return parts.join("\n");
}

// --- Main ---

console.log("Reading field sections from Zod schema...");
const fieldSections = getFieldSections();

console.log("Parsing generated settings table...");
const mdContent = readFileSync(SETTINGS_MD, "utf-8");
const table = parseSettingsTable(mdContent);

console.log("Generating configuration.mdx...");
const mdx = generateMdx(fieldSections, table);
mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
writeFileSync(OUTPUT_FILE, mdx);

console.log(
  `Synced ${table.rows.length} settings across ${SECTIONS.length} sections to configuration.mdx`,
);
